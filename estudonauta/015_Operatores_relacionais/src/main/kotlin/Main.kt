fun main(args: Array<String>) {
    println("Operadores relacionais")
    println("Maior : >")
    println("Menor : <")
    println("Maior Igual: >=")
    println("Menor Igual: <=")
    println("Igual: ==")
    println("Diferente: !=")
    val n1:Int = 5
    val n2:Int = 4
    println("$n1 maior que $n2?  ${n1>n2}")
    println("$n1 menor que $n2?  ${n1<n2}")
    println("$n1 maior ou igual a $n2?  ${n1>=n2}")
    println("$n1 menor ou igual a $n2?  ${n1<=n2}")
    println("$n1 igual a $n2?  ${n1==n2}")
    println("$n1 diferente de $n2?  ${n1!=n2}")
}